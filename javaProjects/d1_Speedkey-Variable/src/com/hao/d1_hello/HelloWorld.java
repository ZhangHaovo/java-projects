package com.hao.d1_hello;

public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("HelloWorld");

        //数值
        System.out.println(666);

        //字符串
        System.out.println("666");

        //字符：单引号中间必须至少有一个字符
        System.out.println(' ');                //        System.out.println('');错误

        //布尔值：true false
        System.out.println(true);
    }
}
